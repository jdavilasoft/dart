
void main() {
  int num1 = 5;
  int num2 = 10;

  print(num1 == num2);
  print(num1 != num2);
  print(num1 < num2);
  print(num1 <= num2);
  print(num1 > num2);
  print(num1 >= num2);

  // Tambien podemos comparar cadenas
  String str1 = "Hola";
  String str2 = "Mundo";

  print(str1 == str2);
  print(str1 != str2);
}