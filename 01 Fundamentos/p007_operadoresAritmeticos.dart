import 'dart:math';

void main() {
  var a = 10;
  var b = 5;

  print("Valor de a = $a");
  print("Valor de b = $b");

  print("");

  var suma = a + b;
  print("La suma es: $suma");

  var resta = a - b;
  print("La resta es: $resta");

  var producto = a * b;
  print("El producto es: $producto");

  var division = a / b;
  print("La división es: $division");

  var resto = a % b;
  print("El resto de la división es: $resto");

  var exponente = pow(a, b);
  print("El exponente es: $exponente");
}